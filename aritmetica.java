class learningaritmetic {
    public static void main(String[] args) {
        
        // Declaración e inicialización de variables enteras
        int a = 10; // Variable a inicializada en 10
        int b = 20; // Variable b inicializada en 20
        int c = 25; // Variable c inicializada en 25
        
        
        // Suma de a y b
        System.out.println("a + b = " + (a + b)); // Imprime: a + b = 30
        
        // Resta de a y b
        System.out.println("a - b = " + (a - b)); // Imprime: a - b = -10
        
        // Multiplicación de a y b
        System.out.println("a * b = " + (a * b)); // Imprime: a * b = 200
        
        // División de b entre a
        System.out.println("b / a = " + (b / a)); // Imprime: b / a = 2
        
        // Módulo de b entre a (resto de la división)
        System.out.println("b % a = " + (b % a)); // Imprime: b % a = 0
        
        // Módulo de c entre a
        System.out.println("c % a = " + (c % a)); // Imprime: c % a = 5
        
        // Incremento de a (pre-incremento)
        System.out.println("a++  = " + (++a)); // Aumenta a en 1 y luego imprime: a++ = 11
        
        // Decremento de b (pre-decremento)
        System.out.println("b--  = " + (--b)); // Disminuye b en 1 y luego imprime: b-- = 19
    }
}
