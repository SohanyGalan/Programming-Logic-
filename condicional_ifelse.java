class Condicionales {
    public static void main(String[] args) {
        // Declaración de la variable 'numero'
        int numero = 5;
        
        // Condicional if-else 
        
        if (numero > 0) {
            System.out.println("El número es positivo");
        } else if (numero < 0) {
            System.out.println("El número es negativo");
        } else {
            System.out.println("El número es cero");
        }
    }
}
